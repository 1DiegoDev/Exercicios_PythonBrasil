# Faça um Programa que peça dois números e imprima a soma.

#Defini a variavel dos dois numeros
num1 = int(input("Digite o número 1: "))
num2 = int(input("Digite o número 2: "))

#E coloquei outra variavel com o valor da soma desses dois
num3 = num1 + num2

#Pra diferenciar eu coloquei uma f string onde pode colocar numa frase variaveis e imprimir o numero 3
print(f"O número da soma é: {num3}")