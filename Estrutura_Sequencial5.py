# Faça um Programa que converta metros para centímetros.

metros = float(input("Digite o número dos Metros: "))

# Dei o valor da variavel de centimetros a conta 
# para discubrir os centímetros é preciso multiplicar metros por 100
centimetros = metros * 100

print(f"O valor de {metros}M para centímetros é: {centimetros}CM")